"""
src/llm/evaluation.py

Deterministic quality checklist for text generated by src/llm/interpreter.py.
Rule-based (not an LLM-as-judge) so it can run in CI without hitting the
Gemini API.
"""

from __future__ import annotations

MIN_LENGTH = 40
MAX_LENGTH = 4000

RISK_LANGUAGE_MARKERS = (
    "risco",
    "probabilidade",
    "indica",
    "sugere",
    "pode",
    "possivelmente",
    "chance",
)


def _numeric_leaves(data) -> list[str]:
    """
    Collect string representations of numeric leaf values found anywhere in
    a (possibly nested) mapping/sequence, using the same str(value)
    formatting as prompts._format_dict_block.
    """
    values: list[str] = []
    if isinstance(data, dict):
        for value in data.values():
            values.extend(_numeric_leaves(value))
    elif isinstance(data, (list, tuple)):
        for value in data:
            values.extend(_numeric_leaves(value))
    elif isinstance(data, (int, float)) and not isinstance(data, bool):
        values.append(str(data))
    return values


def evaluate_explanation(text: str, source_data: dict) -> dict:
    """
    Score a generated explanation against a deterministic quality checklist.

    Rules:
        mentions_recall:    if source_data has a top-level "recall" key, the
                             text must mention "recall" (case-insensitive).
                             Vacuously true otherwise (e.g. per-patient
                             explanations, where source_data is patient_data).
        grounded_in_data:   the text cites at least one numeric value that
                             actually exists in source_data (catches purely
                             generic, non-grounded responses).
        uses_risk_language: the text uses probabilistic/risk framing instead
                             of a bare categorical diagnostic claim.
        reasonable_length:  the text is neither empty/truncated nor
                             excessively long.

    Args:
        text:        the LLM-generated explanation to evaluate.
        source_data: the data the explanation should be grounded in — e.g. a
                     patient_data dict, or
                     {**baseline_metrics, **optimized_metrics, **best_params}.

    Returns a dict: {"score": float in [0, 1], "checks": {rule_name: bool}}.
    """
    lowered = text.lower()

    checks = {
        "mentions_recall": ("recall" not in source_data) or ("recall" in lowered),
        "grounded_in_data": any(number in text for number in _numeric_leaves(source_data)),
        "uses_risk_language": any(marker in lowered for marker in RISK_LANGUAGE_MARKERS),
        "reasonable_length": MIN_LENGTH <= len(text.strip()) <= MAX_LENGTH,
    }

    score = sum(checks.values()) / len(checks)
    return {"score": score, "checks": checks}
